#include "libft.h"

void				ft_strclr(char *s)
{
	int i;

	i = 0;
	if (s)
	{
		while (s[i] != '\0')
			i++;
		while (i >= 0)
		{
			s[i] = '\0';
			i--;
		}
	}
}
